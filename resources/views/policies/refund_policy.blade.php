      <h1>Refund Policy</h1>
      <div class="meta">Varnarc Technologies Pvt Ltd | Last Updated: 08 April 2026</div>

      <p>
        This Refund Policy applies to transactions initiated through the Varnarc mobile application for UPI, credit/debit card,
        net banking, mobile recharge, and bill payments, and is subject to partner bank, payment network, and biller rules.
      </p>

      <h2>1. Eligible Refund Scenarios</h2>
      <ul>
        <li>Transaction debited from user account but marked failed by payment gateway/bank/biller.</li>
        <li>Duplicate debit for the same successful transaction due to technical error.</li>
        <li>Recharge/bill payment failed and amount not auto-reversed within prescribed timeline.</li>
        <li>Confirmed unauthorized transaction after verification and fraud review as per applicable policy and law.</li>
      </ul>

      <h2>2. Non-Refundable Scenarios</h2>
      <ul>
        <li>Incorrect beneficiary details, wrong mobile number, wrong bill account, or wrong amount entered by user.</li>
        <li>Successful transaction confirmed by bank/network/biller without technical or processing fault.</li>
        <li>Service/convenience fees where clearly disclosed and already consumed for completed processing steps.</li>
        <li>Disputes arising solely from merchant/biller service quality after successful payment settlement.</li>
      </ul>

      <h2>3. Refund Processing Timeline</h2>
      <p>
        For eligible transactions, refunds are typically processed within <strong>5 to 7 working days</strong> from confirmation
        of eligibility. In certain cases, timelines may vary based on partner bank, card network, NPCI, or biller reconciliation cycles.
      </p>
      <p class="note">
        Auto-reversal timelines for failed transactions may also be governed by RBI, NPCI, and payment network standards.
      </p>

      <h2>4. Refund Mode</h2>
      <ul>
        <li>Refunds are credited to the original payment source (same bank account/card/UPI instrument), wherever feasible.</li>
        <li>If original source refund is not technically possible, refund will be processed in a compliant alternative mode as per law.</li>
        <li>No cash refunds are provided.</li>
      </ul>

      <h2>5. Disputes and Chargebacks</h2>
      <ul>
        <li>Card and net banking disputes may be subject to issuer bank and network chargeback frameworks.</li>
        <li>UPI disputes may be processed through NPCI/bank grievance mechanisms in addition to Varnarc support workflows.</li>
        <li>Varnarc may request supporting documents, transaction proof, or identity/KYC verification for dispute resolution.</li>
      </ul>

      <h2>6. Compliance and Fraud Safeguards</h2>
      <p>
        Refund requests are reviewed under applicable RBI ecosystem requirements, IT Act 2000 compliance principles,
        internal fraud controls, and KYC/AML standards. Refunds may be withheld or rejected where misuse, policy breach,
        or suspicious activity is detected.
      </p>

      <h2>7. How to Raise a Refund Request</h2>
      <ul>
        <li>In-app support ticket with transaction reference ID, date/time, amount, and issue details.</li>
        <li>Email request to support with registered mobile number and transaction proof.</li>
      </ul>
